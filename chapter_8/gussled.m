
function y = gussled(f, a, b)
	%高斯十点法求函数f从a到b的积分
	n = 10;
	z = [-0.9739065285 -0.8650633677 -0.6794095683 -0.4333953941 ...
		-0.1488743990 -0.1488743990 0.4333953941 ...
		0.6794095683 0.8650633677 0.9739065285];
	w = [0.0666713443 0.1494513492 0.2190863625 0.2692667193 ...
		0.2955242247 0.2955242247 0.2692667193 ...
		0.2190863625 0.1494513492 0.0666713443];
	gg = 0;
	for i = 1 : n
		yy = (z(i) * (b - a) + a + b) / 2;
		gg = gg + w(i) * evalf(f, yy);
	end
	y = gg * (b - a) / 2;
end
